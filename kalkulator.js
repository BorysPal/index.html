
function facet(a,b,c,d) {
    let wynikf = (10*a)+(6.25*b)-(5*c)+5+(1*d);
    let element_txtWynikf= document.getElementById("txtWynikf");
    element_txtWynikf.value=wynikf;
}
function kobieta(a,b,c,d) {
    let wynikk = (10*a)+(6.25*b)-(5*c)-161+(1*d);
    let element_txtWynikk= document.getElementById("txtWynikk");
    element_txtWynikk.value=wynikk;
}
